/**
 * Direct image URL â€” works for publicly shared Drive files.
 * Uses uc?export=view (more reliable than thumbnail API).
 */
function driveImg(id) {
  if (!id) return '';
  if (id.startsWith('http') || id.startsWith('/') || id.includes('.')) {
    return id;
  }
  return `https://drive.google.com/uc?export=view&id=${id}`;
}

/**
 * Convert a viewer-format Drive URL to a direct image URL.
 */
function driveViewerToImg(viewerUrl) {
  const match = viewerUrl && viewerUrl.match(/\/d\/([^/?]+)/);
  const id = match ? match[1] : null;
  if (!id) { return ''; }
  return driveImg(id);
}

function driveVideo(id) {
  if (!id) return '';
  if (id.startsWith('http') || id.startsWith('/') || id.includes('.')) {
    return id;
  }
  return `https://drive.google.com/file/d/${id}/preview`;
}

